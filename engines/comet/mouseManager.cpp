	bool MouseManager::updateMouseStatus() {
		_currPos = _evtMgr->getMousePos();
		int tmp = _evtMgr->getButtonState();
		_leftButStatus = _rightButStatus = false;
		if (tmp & 0x01) {
			_leftButStatus = true;
		}
		if (tmp & 0x02) {
			_rightButStatus = true;
		}
		return true;
	}
	

